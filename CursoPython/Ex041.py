print('Confederação de natação, veja a categoria por idade:')

idade = int(input('Digite sua idade: '))

if idade < 11:
    print(f'Até {idade} anos: MIRIM')
elif idade <= 16:
    print(f'Até {idade} anos: JUNIOR')
elif idade <= 19:
    print(f'Até {idade} anos: SENIOR')
elif idade is 20:
    print(f'Até {idade} anos: MASTER')